    <script>
     document.addEventListener("DOMContentLoaded", function () {
    const selectedSeats = [];
    function toggleSeat(seatId) {
        const seat = document.getElementById(seatId);
        if (seat.classList.contains("selected")) {
            seat.classList.remove("selected");
            const index = selectedSeats.indexOf(seatId);
            if (index !== -1) {
                selectedSeats.splice(index, 1);
            }
        } else {
            seat.classList.add("selected");
            selectedSeats.push(seatId);
        }
        updateCountAndTotal();
    }
    function updateCountAndTotal() {
        const countElement = document.getElementById("count");
        const totalElement = document.getElementById("total");
        const count = selectedSeats.length;
        const total = count * 20;
        countElement.textContent = count;
        totalElement.textContent = total;
    }
    function markReservedSeats() {
    const selectedMovieOption = document.getElementById("movie");
    const movieTime = selectedMovieOption.value;

    fetch('./CheckReservedSeats.php', {
        method: 'POST',
        headers: {
            'Content-Type': 'application/json',
        },
        body: JSON.stringify({ movieTime }),
    })
    .then(response => response.json())
    .then(reservedSeats => {

        reservedSeats.forEach(seatId => {
            const reservedSeat = document.getElementById(seatId + '-' + movieTime);
            if (reservedSeat) {
                reservedSeat.classList.add("sold");
            } else {
            }
        });
    })
    .catch((error) => {
    });
}
    document.querySelectorAll('.seat').forEach(seat => {
        seat.addEventListener('click', () => {
            const seatId = seat.id;
            toggleSeat(seatId);
        });
    });

    document.getElementById("confirmButton").addEventListener("click", function () {
        const movieTime = document.getElementById("movie").value;
        const postData = {
            selectedSeats: selectedSeats,
            movieTime: movieTime
        };

        fetch('./skryptdziekcz.php', {
            method: 'POST',
            headers: {
                'Content-Type': 'application/json',
            },
            body: JSON.stringify(postData),
        })
        .then(response => response.text())
        .then(data => {
            markReservedSeats();
        })
        .catch((error) => {
        });
    });

    markReservedSeats();
});
